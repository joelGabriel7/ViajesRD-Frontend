class Agency {
  final int? id;
  final String name;
  final String address;
  final String phone;
  final String email;
  final String logo;
  final String rnc;

  Agency({
    this.id,
    required this.name,
    required this.address,
    required this.phone,
    required this.email,
    required this.logo,
    required this.rnc,
  });
}
